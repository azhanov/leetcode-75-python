def find_stacks_dimensions(input):
    lines = input.split('\n')
    # find line with numbers
    stack_depth = 0
    for idx, line in enumerate(lines):
        first_char = line.strip()[0]
        if first_char.isnumeric():
            break
        else:
            stack_depth = idx

    # find width
    columns_count = line.strip().split(' ')[-1]
    return stack_depth, int(columns_count)


def parse_out_stacks(input, stack_depth, width):
    stacks = []
    for stack_idx in range(width):
        stacks.append([])

    lines = input.split('\n')
    # move up from the row containing stack numbers
    for row_idx in range(stack_depth, -1, -1):
        # 0, 4, 8
        # 1,    2,    3
        # 0     1     2
        # 0*4   1*4   2*4
        # [0:3] [4:7] [8:11]
        for i in range(0, width):
            crate = lines[row_idx][i*4: i*4 + 3]
            if crate.strip() != '':
                stacks[i].append(crate)
    return stacks


def input_to_steps(input, depth):
    steps = []
    lines = input.split('\n')
    for row_idx, line in enumerate(lines):
        if row_idx <= depth + 2:
            continue
        steps.append(line)
    return steps


def move_crates(stacks, instructions):
    for instruction in instructions:
        crates_num = int(instruction.split()[1])
        crates_from = int(instruction.split()[3]) - 1
        crates_to = int(instruction.split()[5]) - 1
        for crate_i in range(crates_num):
            crate_to_move = stacks[crates_from].pop()
            stacks[crates_to].append(crate_to_move)
    return stacks


def move_crates_ext(stacks, instructions):
    for instruction in instructions:
        crates_num = int(instruction.split()[1])
        crates_from = int(instruction.split()[3]) - 1
        crates_to = int(instruction.split()[5]) - 1
        stack = []
        for crate_i in range(crates_num):
            crate_to_move = stacks[crates_from].pop()
            stack.append(crate_to_move)
        stack = reversed(stack)
        for crate in stack:
            stacks[crates_to].append(crate)
    return stacks


def print_results(stacks, width):
    result = ''
    for col in range(width):
        result += (stacks[col].pop()[1])
    print(result)


def execute(input):
    stack_depth, width = find_stacks_dimensions(input)
    stacks = parse_out_stacks(input, stack_depth, width)
    print(stacks)
    instructions = input_to_steps(input, stack_depth)
    stacks = move_crates_ext(stacks, instructions)
    print(stacks)
    print_results(stacks, width)


if __name__ == '__main__':
    execute(input)
